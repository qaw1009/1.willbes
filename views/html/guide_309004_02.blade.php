                    그리고 제시간내에 문제풀이가 불가능한 경우도 많으므로 시험시간의 부족을 초래하여 시간 배정을 잘못할 경우 다른 과목에도 영향을 주는 과목이다. 
                    회계학 고득점을 위하여 다음 사항에 유의하여 시험준비를 해야 한다. 
                </div>
                <ul class="st01 mt10">
                    <li>
                        <span class="tx-blue">기초적인 개념(회계원리 부분)을 정확히 이해해야 한다. </span><br>
