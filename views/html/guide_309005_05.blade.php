                    <h5 class="NGR mt20">공부방법</h5>
                    <div>
                        회계는 보통 회계원리, 재무회계, 원가회계가 들어간다. 원회계같은경우 10문제 정도 나오는데 원가만 잡아도 과락을 면할 가능성이 높아 진다. 
                        보통 재무에 집중하고 원가를 소홀히 하기 때문에 막상 원가가 쉽고 빠른 시간에 풀수 있는데도 불구하고 시험에서 갈피를 못잡는 경우가 많다. 
                        그리고 재무의 경우 일명 기타회계라 불리는 법인세 부분등과 같은 경우 과감히 스킵해버리고 감가상각 이자 쪽에 확실히 투자하는 것도 괜찮은 방법이 될 수 있겠다. 
                        회계학은 타시험에 비해 서술형비중이 높다는 특징이 있으므로, 서술형문제에 대한 철저한 대비가 필요하다. 서술형 문제는 기본강의를 통해 기초를 쌓고 시험 직전 
                        서술형 문제만을 대상으로 하는 특강을 수강하는 것이 가장 효율적인 대비방안이다. 
                        회계의 경우 한번 시작하면 매일 하루에 10분이라도 봐야 날라가지 않으니 꾸준하게 공부할 수 있도록 하자.
