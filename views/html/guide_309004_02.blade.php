                    <li>
                        <span class="tx-blue">민법공부를 통한 Legal Mind 형성</span><br>
                        민법공부를 통해 법학적 사고방식 내지 법학적 논리구조를 체득해야 한다. 민법공부가 다른 법과목공부에 선행되어야 하는 것은 민법공부를 통한 Legal Mind 형성이 
                        다른 법과목을 정복하는 지름길이 되기 때문이기도 하다. 또한 Legal Mind의 형성은 객관식 시험이든 주관식 시험이든 법과목 시험에 있어서 예상치 못한 문제의 해결을 위해서도 필수적이다.
                    </li>
                </ul>
                <h5 class="NGR mt20">가. 민법공부의 정석 : 이해 → 정리 → 반복</h5>
                <ul class="st01">
                    <li>
                        <span class="tx-blue">이해</span><br>
                        속독에 의한 다독은 법과목 시험을 치기 전에 반드시 거쳐야 할 과정임에는 틀림없다. 그러나 먼저 정독에 의한 이해가 선행되어야 한다. 
                        이해되지 않은 상태에서의 속독은 단지 “활자를 보고 지나가는 것”에 불과하다.
                    </li>
                    <li>
                        <span class="tx-blue">정리</span><br>
                        이해만 하고 그 내용을 정리하지 않으면, 반복을 통한 암기를 할 수 없다. 1,800페이지가 넘는 교과서를 통째로 암기할 수 없는 바에야, 그 내용을 압축 · 정리하는 것은 필수적이다.
                    </li>
                    <li>
                        <span class="tx-blue">반복</span><br>
                        법과목, 특히 민법은 한 번의 이해와 정리로 끝낼 수 있는 것이 아니다. 이해 · 정리되지 않았던 부분들도 보완할 수 있다.
                    </li>
                </ul>
                <h5 class="NGR mt20">다. 민법공부의 중요한 대상들 : 법조문 → 판례 → 통설</h5>
                <ul class="st01">
                    <li>
                        <span class="tx-blue">법조문</span><br>
                        수험법학은 법조문을 해석하는 해석법학이다. 민법 교과서도 법조문의 의미를 보다 자세히 풀어 해석하고 거기에 법전이 규정하고 있지 않은 것을 보충하면서 
                        체계를 세운 것에 불과하다. 특히 민법은 방대한 양의 조문체계를 갖춘 만큼, 법조문은 그 자체가 곧 문제가 되고 답이 된다. 성문법주의 국가인 우리나라에서 법조문은 판례와 학설에 우선한다.
                    </li>
                    <li>
                        <span class="tx-blue">판례</span><br>
                        “다툼이 있으면 판례에 의한다.” 이는 모든 법과목 객관식 시험에서 제시되는 기준이다. 명시적으로 위와 같은 기준이 제시되지 않더라도 학설 대립이 있으면, 판례가 답이 된다. 즉, 판례는 학설에 우선한다. 
                    </li>
                    <li>
                        <span class="tx-blue">통설</span><br>
                        실제 생활에서 법률적 분쟁이 발생하지 않았거나, 또는 발생했더라도 법원에 그 해결을 청구하지 않았다면, 그와 관련된 판례는 존재하지 않는다. 이러한 경우에는 통설(=일반적으로 인정되고 있는 학설)이 답이 된다.
                    </li>
                </ul>
