                        <span class="tx-blue">민법의 중요성 : 산업재산권법 · 민사소송법의 기초 내지 전제</span><br>
                        민법은 법과목 중에서도 가장 기본적이고 중요한 과목이다. 변리사시험에 있어서도 민법상의 기본적 개념들에 대한 이해가 전제되지 않고서는 
                        산업재사권법 · 민사소송법 등에 대한 접근이 어렵다. 즉, 민법은 산업재산권법 · 민사소송법의 기초 내지 전제로서, 민법공부가 다른 법과목공부에 선행되어야 한다.
