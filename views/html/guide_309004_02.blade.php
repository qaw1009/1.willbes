                        또한 2차 실무시험에서 회계학 출제문제는 회계원리의 재무제표부분이므로 철저한 학습이 필요하다.
                    </li>
                    <li>
                        <span class="tx-blue">중요부분을 전략적으로 공부해야 한다. </span><br>
