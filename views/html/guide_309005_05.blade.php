                <div id="stabB01">
                    <h4 class="NGR">관세법</h4>
                    <h5 class="NGR">출제경향</h5>
                    <div>
                        관세법이라는 과목특성상 법상 제도에 대해 서술하는 단순 서술식의 문제는 그 중요성은 언급할 필요가 없다. 
                        관세법 및 환특법상 조문에 대한 이해와 암기가 필요하며 관세법 내 규정 또는 관세법과 환특법상의 비교문제 또한 종종 출제되고 있다. 
                        이러한 비교문제는 그 범위를 법에 한정하여 조리 있게 서술하시는 것이 중요하다. 비교 가능한 제도를 모아서 대비해 보는것도 좋은 방법 이다. 
                        관세법은 1,2차 시험과목에 모두 포함되는데, 제1차 객관식 시험에서는 ‘관세법개론’으로 출제되며, 제2차 주관식 시험의 ‘수출용 원재료에 대한 환급특례법’은 제외 되어 출제된다.
                    </div>
                    <h5 class="NGR mt20">공부방법</h5>
                    <div>
                        국관세사 시험에서 관세법은 그 명칭에서도 알 수 있듯이 가장 중요한 과목이고, 수험생들이 관세사 시험 준비를 시작하면서 가장 먼저 공부를 시작하는 과목 중의 하나이다. 
                        관세법은 이를 처음 접하는 수험생들은 생소한 전문용어들로 인하여 그 체계를 이해하기가 어려우나 꾸준한 학습과 학원강의 등을 통하여 그 흐름을 잡으면 비교적 쉽게 전체를 이해할 수 있는 과목이다.
                        실제로 시험을 치른 수험생들에 의하면 시험과목 중 관세법이 가장 고득점이 가능한 과목이라는 견해가 대부분이다.
                    </div>
                    <ul class="st01 mt20">
                        <li>
                            <strong class="tx-blue">기본에 충실하기</strong><br>
                            관세법 2차 과목의 출제경향을 보면 단순 서술식의 문제가 많이 출제되었으며, 
                            향후에도 충분히 출제 가능성이 높기 때문에 법조문을 충분히 이해하고 암기하고 있어야 한다. 겸손하게 자만하지 말고 기본에 충실히 공부를 하자.
                        </li>
                        <li>
                            <strong class="tx-blue">비교문제대비</strong><br>
                            비교문제 또한 준비가 필요한 부분이다. 과연 어디까지 준비 해야할까? <br>
                            범위와 내용을 크게잡지 말고 비교문제로서 가치가 있는 문제나 출제 가능성이 높다고 생각되는 문제면 충분하다. <br>
                            다양한 비교문제는 오히려 여러분들에게 공부방향을 흐리게 하는 등 독이 될 수 있다.
                        </li>
                        <li>
                            <strong class="tx-blue">사례를 통한 문제</strong><br>
                            최근 출제되고 있는 사례문제를 대비하기 위하여 각 제도에서 규정하고자 하는 바를 정확하게 인지하여야 한다. 
                            관세법상 각 제도를 충분히 이해하고 있어야 함은 물론이고 보충적으로 관련 판례 등을 통하여 많은 사례를 접해보는 노력이 필요하다.
                        </li>
                        <li>
                            <strong class="tx-blue">환특법에 소홀하지 말것</strong><br>
                            매년 1문제 이상 출제되고 있는 관세환급특례법은 그 범위에 비해서 출제가 확실한 부분임에도 불구하고 관세법에 비해 소홀히 하는 경향이 있기 때문에 
                            환특법에 대한 상세한 설명 및 에상문제 정리 및 환특법 문제뿐만 아니라 관세법과의 비교문제 출제시 놓치는 일이 없도록 철저히 준비해야 한다.
                        </li>
                        <li>
                            <strong class="tx-blue">기출문제의 활용</strong><br>
                            기존의 기출문제로는 최근의 출제 경향에 적응하기 힘들고 또 같은 문제가 나올 가능성은 적지만 같은 사항을 묻는 문제가 출제될 가능성은 언제나 있으므로 반드시 모두 풀어 보아야 한다.
                        </li>
                    </ul>
