                <div id="stabB02">
                    <h4 class="NGR">관세율표 및 상품학</h4>
                    <h5 class="NGR">출제경향</h5>
                    <div>
                        관세율표 및 상품학 문제는 전반적으로 기본에 충실한 공부를 했는지를 평가하는 문항이 많이 출제 된다. 
                        러므로 무조건 답안지 양을 많이 작성하는 것이 중요한 것이 아니라 문제의 핵심적인 사항을 주로 호의 용어, 통식에 근거해 정확히 작성하는 것이 필요하다.
                    </div>
                    <h5 class="NGR mt20">공부방법</h5>
                    <div>
                        관세율표 및 상품학 수험준비에 있어서는 숲과 나무를 동시에 볼 수 있는 전략이 필요하다.
                        특히 21개의 부와 96개의 류의 표제에 대해서는 도표 및 그림을 통해서라도 암기하는 것이 중요하다. HS의 각 부와 각 류의 표제 정도는 외워야 한다. 
                        HS관세율표의 내용은 상당히 방대하다.
                        부의 표제나 류의 표제가 암기되어 있는 지를 알아보기 위한 문제가 나오는 것은 아니지만, 이것의 암기여부는 HS관세율표 전체의 골격을 잡는 데 크게 도움이 될 것이다. 하지만 2단위 즉, 류의 표제까지만 암기하면 된다. 
                        HS관세율표를 공부하려면 HS관세율표의 분류를 실제로 봐야한다. 4단위 기준으로 된 ‘HS 분류실제’를 봐가며 실제로 어떠한 상품들이 어떻게 분류되는 지를 경험하라. 
                        2단계까지 학습이 이루어졌어도 아직은 이해가 가지 않는 부분이 있을 것이다. 
                        이럴 때에는 교재의 내용을 찬찬히 음미해 가며 정독한다. 도표나 그림을 이용하여 이제까지 학습한 것을 최종 정리한다. 
                        이제는 수차례 반복하면서 이제까지 학습한 내용을 정리하는 단계이다. 
                        정리하는 단계에서는 제1류부터 제97류까지 순서대로 정리하는 것보다는 주요 핵심 사항별로 HS관세율표 전체에 걸쳐 정리하는 것이 바람직하다. 
                        본인 나름대로 중요 내용에 대해서 문제를 직접 출제해 보고, 이에 대한 채점 기준표도 만들어 본다. 
                        아마 HS관세율표를 보는 시각이 달라질 것이다. 
                        아는 것과 쓰는 것은 나르다. 실제 답안지를 준비하고, 시계를 옆에 놓고, 실제 시험을 치른다는 각오로 답안을 작성한다.
                        이러한 경험은 실제 수험장에서 그 진가를 발휘하게 된다. 
                    </div>
                    <ul class="st01 mt20">
                        <li>
                            <strong class="tx-blue">이해, 암기, 회독수</strong><br>
                            2차 시험은 백지에 자신이 암기한 내용을 이해와 논리를 이용하여 표현하는 것이다. 
                            타 과목과 비교하여 암기에 조금 더 비중이 있는 과목이다. 암기라고 표현을 한 것은 상기 예시로 열거한 통칙, 주, 호의 용어의 내용이 조금이라도 틀려지면 실제 품목분류시 오류가 발생하기 때문이다.
                            그렇다고 무작정 이해없이 암기하기 보다는 해당 류의 구성을 파악하고 분류 배열을 명확히 이해한다면 연결 고리를 물어 정확한 서술이 가능할 것이다. 
                            또한 타 과목에 비해 정확한 암기와 이해가 되어있는 경우 정리를 가장 빨리 끝낼 수 있는 과목이다. 
                        </li>
                        <li>
                            <strong class="tx-blue">정리</strong><br>
                            해당 과목에 대한 서브 노트는 각 자만의 방식이 있으므로 중립적인 입장이지만 해당 부 또는 류에 대한 주 규정 및 중요 해설서 내용은 
                            목차만이라도 미리 정리 하는걸 추천한다. 미리 정리하지 않으면 실제 시험시 분명히 서술 가능한 부분을 빼먹기 쉽다.
                            또한 주 규정 및 호의 용어는 중요 부분을 미리 정리 하여 반복적으로 보지 않으면 잘 암기 했던 내용도 한 순간 기억이 나지 않는다. 
                            따라서 주 규정 및 호의 용어는 수험 기간 동안 항상 옆에 끼고 있어야 한다. 
                        </li>
                        <li>
                            <strong class="tx-blue">쓰기 연습</strong><br>
                            열심히 공부한 내용을 머릿속으로 생각만하면 이를 말로써 정확하게 표현하는 사람보다 시험장에서 불리하며 말로 표현 할 수 있고 
                            최종 목표인 글로서 정확한 서술이 가능하다면 이를 해낼 수 있는 사람은 합격권이다.
                            또한 글씨의 모양은 상당히 중요하다. 80분동안 A4지 8장을 앞뒤로 가득 쓰는 것은 엄청난 속도가 요구 된다. 
                            그러기에 글씨는 제한된 시간내에 무너져 내리기 쉽상이다. 그러기에 쓰기 연습이 중요하며 객관화를 위하여 모의고사 강좌를 일반적으로 이용한다.
                        </li>
                    </ul>
                </div>

                <div id="stabB03">
                    <h4 class="NGR">관세평가</h4>
                    <h5 class="NGR">출제경향</h5>
                    <div>
                        매년 어려운 난이도로 수험생 성적의 편차가 많이 나는 관세평가의 경우에는 최근 문제가 실무에 너무 치중된 것을 감안해 지난해에는 법규정 이해도와 실무 계산 능력을 동시에 질문하는 문제가 많이 출제되었다. 
                        앞으로는 이와 비슷한 수준 또는 실무보다 법규정 이해에 더욱 초점을 맞춘 문제가 나올 것으로 전망된다.
                    </div>
                    <h5 class="NGR mt20">공부방법</h5>
                    <div>
                        관세평가는 관세사 2차 시험 중 한 과목으로써 큰 비중을 차지하고 있으며, 과세관청이나 관세사들도 이쪽으로 관심을 쏟아 붓고 있는 것도 관세평가의 중요성을 말해준다.
                        관세평가 과목이 2차 시험에 포함된 얼마 되지 않아서 2차 다른 과목(관세법, HS, 무역실무)에 비해 분량이 상당히 적다. 관세평가는 전문성을 요하며, 응용하여 출제할 문제들이 많다.
                        기출문제를 분석해보면 단순히 “무엇에 대해 설명하시오”하는 문제는 그리 많지 않았다. 
                        사례를 들어서 문제를 출제했으며, 어느 정도의 숫자도 포함된 문제들이 대부분 이였다. 어떻게 하면 시험장에서 생소한 응용문제를 접해도 당황하지 않고 답안을 작성할 수 있을까. 
                        정답은 사례들을 많이 접해보며 내가 이해하고 외운 법조문들을 적용해 보는 것이다.
                    </div>
                    <ul class="st01 mt20">
                        <li>
                            <strong class="tx-blue">정리</strong><br>
                            관세평가 관련 관세청 유권해석이나 대법원 판례 등 소스들은 수 없이 많다. 
                            이를 간추려 시험문제로 출제될 가능성이 높은 사례들만 교재에 실어 자주 다뤄 보는데 중점을 두어야 겠다.
                        </li>
                        <li>
                            <strong class="tx-blue">기출문제</strong><br>
                            관세평가 시험에 계산기까지 가져 들어가도록 하고 있다. 간단한 산수를 요하는 문제지만 어쨌거나 숫자를 이용하여 계산을 하도록 하고 있다. 
                            그래서 계산문제까지 실어서 자주 접해 봄으로서 적응력을 키울 수 있도록 하여야 한다.
                        </li>
                    </ul>
                </div>

                <div id="stabB04">
                    <h4 class="NGR">무역실무</h4>
                    <h5 class="NGR">출제경향</h5>
                    <div>
                        관세사 시험에서 출제하는 무역실무의 범위는 실제 무역 업무처리에 필요한 기능을 측정하기 보다는 관세사로서의 직무를 수행하는 데 필요한 무역실무에 관한 지식 및 법리적 
                        상무적인 지식을 측정하는 데 중점이 있으며, 관세법 관련 내용, 대외무역법관련내용, 외국환관리법 관련내용은 타과목으로 채택되어 있으므로 그 범위에서 제외된다고 할 수 있다.
                        관세사 제2차시험에서 무역실무 과목은 과락이 가장 많이 나오는 과목으로서 타 과목과 달리 법률이 아니기 때문에 범위면에서 가장 넓다고 할 수 있으며, 학습해야할 부분이 가장 많은 과목이다.
                    </div>
                    <h5 class="NGR mt20">공부방법</h5>
                    <div>
                        무역실무는 무역관련 모든 분야를 총망라한 과목으로 매우 깊이 있는 부분까지 학습하여야 한다. 
                        단순히 대학 전공 교과 수준의 내용을 학습하고 수험 준비를 한다면 높아지는 난이도에 적응할 수 없다. 
                        다른 2차 과목과 마찬가지로 상당히 깊이 있는 부분까지의 학습이 필요한 것은 당연하다. 하지만 무역실무는 처음부터 암기한다는 생각을 해서는 안 된다. 
                        상인들이 실제 거래에 적용하는 내용이기 때문에 처음부터 학자들이 개입이 된 것은 아니다. 
                        그렇기 때문에 학문적으로 접근해서는 안 된다. 국제 무역의 한 주축으로 내가 상인이라는 관점에서 이해를 하여야 한다. 
